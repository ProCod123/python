number = int(input('Введите число: '))

f1, f2 = 0, 1
fibo = [1, 0, 1] # Предопределенные цифры
for i in range(1,number):
    f1, f2 = f2, f1 + f2
    fibo.append(f2) 
    fibo.insert(0, ((-1) ** i) * f2) #Нахождение негофибо. от фибо. F(−n) = (−1)^(n+1) * Fn
print('Результат: ', fibo)

